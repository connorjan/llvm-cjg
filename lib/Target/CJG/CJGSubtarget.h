#ifndef CJGSUBTARGET_H
#define CJGSUBTARGET_H

#include "CJGFrameLowering.h"
#include "CJGISelLowering.h"
#include "CJGInstrInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/Target/TargetMachine.h"
#include "llvm/Target/TargetSubtargetInfo.h"

#define GET_SUBTARGETINFO_HEADER
#include "CJGGenSubtargetInfo.inc"

namespace llvm {
class StringRef;

class CJGSubtarget : public CJGGenSubtargetInfo {
  virtual void anchor();

  CJGInstrInfo InstrInfo;
  CJGFrameLowering FrameLowering;
  CJGTargetLowering TLInfo;

public:
  // Initializes the data members to match that of the specified triple.
  CJGSubtarget(const Triple &TT, StringRef CPU,
               StringRef FS, CJGTargetMachine &TM);

  // Parses features string setting specified subtarget options. The
  // definition of this function is auto-generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef FS);

  const CJGFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const CJGInstrInfo *getInstrInfo() const override {
  	return &InstrInfo;
  }
  const TargetRegisterInfo *getRegisterInfo() const override {
    return &InstrInfo.getRegisterInfo();
  }
  const CJGTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
};
} // End llvm namespace

#endif
